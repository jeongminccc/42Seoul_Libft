char	*ft_strlowcase(char *str)
{
	int pos;

	pos = 0;
	while (str[pos])
	{
		if ('A' <= str[pos] && str[pos] <= 'Z')
			str[pos] += 32;
		++pos;
	}
	return (str);
}
