int	ft_str_is_alpha(char *str)
{
	if (*str == '\0')
		return (1);
	while (*str)
	{
		if (!(*str <= 'z' && *str >= 'a') && !(*str <= 'Z' && *str >= 'A'))
			return (0);
		str++;
	}
	return (1);
}
